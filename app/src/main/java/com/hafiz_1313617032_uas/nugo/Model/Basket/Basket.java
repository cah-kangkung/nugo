package com.hafiz_1313617032_uas.nugo.Model.Basket;

public class Basket {

    private String food_name;
    private String food_energy;
    private String food_protein;
    private String food_fat;
    private String food_carbo;
    private String food_image;
    private int food_count;

    public String getFood_name() {
        return food_name;
    }

    public void setFood_name(String food_name) {
        this.food_name = food_name;
    }

    public String getFood_energy() {
        return food_energy;
    }

    public void setFood_energy(String food_energy) {
        this.food_energy = food_energy;
    }

    public String getFood_protein() {
        return food_protein;
    }

    public void setFood_protein(String food_protein) {
        this.food_protein = food_protein;
    }

    public String getFood_fat() {
        return food_fat;
    }

    public void setFood_fat(String food_fat) {
        this.food_fat = food_fat;
    }

    public String getFood_carbo() {
        return food_carbo;
    }

    public void setFood_carbo(String food_carbo) {
        this.food_carbo = food_carbo;
    }

    public String getFood_image() {
        return food_image;
    }

    public void setFood_image(String food_image) {
        this.food_image = food_image;
    }

    public int getFood_count() {
        return food_count;
    }

    public void setFood_count(int food_count) {
        this.food_count = food_count;
    }
}
